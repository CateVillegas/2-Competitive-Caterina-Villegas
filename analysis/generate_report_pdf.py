def main() -> None:
    parser = argparse.ArgumentParser(description="Genera el PDF ejecutivo de insights")
    parser.add_argument("--output", help="Ruta final del PDF", default=str(OUTPUT_PDF))
    args = parser.parse_args()

    output_dir = Path("output")
    kpis, insights = load_payloads(output_dir)
    styles = build_styles()

    doc = SimpleDocTemplate(
        args.output,
        pagesize=A4,
        leftMargin=MARGIN,
        rightMargin=MARGIN,
        topMargin=MARGIN,
        bottomMargin=2 * cm,
    )
    story = build_story(kpis, insights, styles, output_dir)
    doc.build(story, onFirstPage=cover_page, canvasmaker=NumberedCanvas)
    print(f"[OK] PDF generado -> {args.output}")


if __name__ == "__main__":
    main()

def load_payloads(output_dir: Path) -> Dict:
    kpis_path = output_dir / "kpis.json"
    insights_path = output_dir / "top_insights.json"
    if not kpis_path.exists():
        raise FileNotFoundError("Ejecuta generate_analysis.py antes de armar el PDF")
    with kpis_path.open(encoding="utf-8") as fh:
        kpis = json.load(fh)
    insights = []
    if insights_path.exists():
        with insights_path.open(encoding="utf-8") as fh:
            payload = json.load(fh)
            insights = payload.get("insights", [])
    return kpis, insights



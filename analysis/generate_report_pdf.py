def chart_gallery_full(kpis: Dict, output_dir: Path, styles) -> List:
    """Include ALL charts with their descriptions and interpretations."""
    gallery: List = []
    chart_files = kpis.get("charts", [])  # No limit — include all charts
    for chart in chart_files:
        if not chart:
            continue
        path = output_dir / chart
        if not path.exists():
            continue

        stem = path.stem
        desc = CHART_DESCRIPTIONS.get(stem, {})
        chart_title = desc.get("title", stem.replace("_", " ").replace("chart ", "").title())
        purpose = desc.get("purpose", "")
        relevance = desc.get("relevance", "")

        elements = []
        elements.append(Paragraph(chart_title, styles["Section"]))

        if purpose:
            elements.append(Paragraph(f"<b>Qué muestra:</b> {purpose}", styles["ChartPurpose"]))
        if relevance:
            elements.append(Paragraph(f"<b>Relevancia para Strategy/Pricing:</b> {relevance}", styles["ChartRelevance"]))

        img = Image(str(path), width=15 * cm, height=8.5 * cm)
        elements.append(img)
        elements.append(Spacer(1, 0.4 * cm))

        gallery.append(KeepTogether(elements))
    return gallery



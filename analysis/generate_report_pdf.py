class NumberedCanvas(pdfcanvas.Canvas):
    """Adds footer with page numbers and confidentiality banner."""

    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        self._pages = []

    def showPage(self):
        self._pages.append(dict(self.__dict__))
        self._startPage()

    def save(self):
        total = len(self._pages)
        for state in self._pages:
            self.__dict__.update(state)
            if self._pageNumber > 1:
                self.setFont("Helvetica", 7)
                self.setFillColor(colors.HexColor("#6F7391"))
                self.drawRightString(A4[0] - MARGIN, 1.0 * cm, f"Pag. {self._pageNumber}/{total}")
                self.drawString(MARGIN, 1.0 * cm, "CONFIDENCIAL · Competitive Intelligence Rappi")
                self.setStrokeColor(colors.HexColor("#D7DAE7"))
                self.setLineWidth(0.4)
                self.line(MARGIN, 1.3 * cm, A4[0] - MARGIN, 1.3 * cm)
            super().showPage()
        super().save()



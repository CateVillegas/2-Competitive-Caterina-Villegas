def insights_section(insights: List[Dict], styles) -> List:
    blocks: List = []
    if not insights:
        blocks.append(Paragraph("No se generaron insights automáticamente.", styles["Body"]))
        return blocks
    for idx, ins in enumerate(insights, start=1):
        title = Paragraph(f"{idx:02d}. {ins['title']}", styles["InsightTitle"])
        finding = Paragraph(f"<b>Finding:</b> {ins['finding']}", styles["InsightSub"])
        impact = Paragraph(f"<b>Impacto:</b> {ins['impact']}", styles["InsightSub"])
        rec = Paragraph(f"<b>Recomendación:</b> {ins['recommendation']}", styles["InsightSub"])
        blocks.append(KeepTogether([title, finding, impact, rec, Spacer(1, 0.2 * cm)]))
    return blocks


